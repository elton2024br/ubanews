export const getNewsByCategory = (news: NewsArticle[], category: string): NewsArticle[] => {
  if (category === 'Todas') return news;
  return news.filter(article => article.category === category);
};

export const getFeaturedNews = (news: NewsArticle[]): NewsArticle[] => {
  return news.filter(article => article.featured);
};

export const getNewsById = (id: string): NewsArticle | undefined => {
  return newsArticles.find(article => article.id === id);
};

export const searchNews = (news: NewsArticle[], query: string): NewsArticle[] => {
  if (!query.trim()) return news;
  
  const searchTerm = query.toLowerCase().trim();
  return news.filter(article => 
    article.title.toLowerCase().includes(searchTerm) ||
    article.excerpt.toLowerCase().includes(searchTerm) ||
    article.content.toLowerCase().includes(searchTerm) ||
    article.tags.some(tag => tag.toLowerCase().includes(searchTerm)) ||
    article.category.toLowerCase().includes(searchTerm)
  );
};

// Advanced search with multiple filters
export const advancedSearchNews = (
  news: NewsArticle[], 
  query: string, 
  category?: string, 
  dateRange?: { start: Date; end: Date }
): NewsArticle[] => {
  let filteredNews = news;
  
  // Apply text search
  if (query.trim()) {
    filteredNews = searchNews(filteredNews, query);
  }
  
  // Apply category filter
  if (category && category !== 'Todas') {
    filteredNews = getNewsByCategory(filteredNews, category);
  }
  
  // Apply date range filter
  if (dateRange) {
    filteredNews = filteredNews.filter(article => {
      const articleDate = new Date(article.date);
      return articleDate >= dateRange.start && articleDate <= dateRange.end;
    });
  }
  
  return filteredNews;
};

// Get search suggestions based on partial query
export const getSearchSuggestions = (query: string, limit: number = 5): string[] => {
  if (!query.trim()) return [];
  
  const searchTerm = query.toLowerCase().trim();
  const suggestions = new Set<string>();
  
  newsArticles.forEach(article => {
    // Add matching titles
    if (article.title.toLowerCase().includes(searchTerm)) {
      suggestions.add(article.title);
    }
    
    // Add matching tags
    article.tags.forEach(tag => {
      if (tag.toLowerCase().includes(searchTerm)) {
        suggestions.add(tag);
      }
    });
    
    // Add matching categories
    if (article.category.toLowerCase().includes(searchTerm)) {
      suggestions.add(article.category);
    }
  });
  
  return Array.from(suggestions).slice(0, limit);
};